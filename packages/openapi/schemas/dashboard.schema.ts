import { z } from '../zod'

/**
 * Dashboard layout schema representing a user's dashboard layout
 * It includes fields for the layout details such as version, layout structure, and timestamps
 */
export const DashboardLayoutSchema = z
    .object({
        id: z.string().uuid(),
        userId: z.string().uuid(),
        version: z.number(),
        layout: z.string(),
        createdAt: z.string().datetime(),
        updatedAt: z.string().datetime()
    })
    .openapi('DashboardLayout')

/**
 * Request schema for updating a dashboard layout
 * It omits fields that are automatically generated by the system (id, userId, createdAt, updatedAt)
 */
export const UpdateDashboardLayoutRequestSchema = DashboardLayoutSchema.omit({
    id: true,
    userId: true,
    createdAt: true,
    updatedAt: true
}).openapi('UpdateDashboardLayoutRequest')
